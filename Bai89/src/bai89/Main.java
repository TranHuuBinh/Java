    private String GetValuePhanXuong()
    {
        switch (cbbphanxuong.getSelectedIndex()) 
        {
            case 0:
                return "A";
            case 1:
                return "B";
            case 2:
                return "C";
            default:
                return "D";
        }
    }
    
    private boolean CheckVoid()
    {
        return !"".equals(txtmanv.getText()) && !"".equals(txtsosp.getText());
    }
    
    private boolean CheckContainMaNV(String temp)
    {
        boolean check=true;
        int a = table.getModel().getRowCount();
        for (int i=0; i<a; i++)
            if (temp.equals(table.getModel().getValueAt(i,0)))
            {
                check=false;
                break;
            }
        return check;
    }
    
