    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        // TODO add your handling code here:
        if (CheckVoid() == true)
        {
            try
            {
                Integer.parseInt(txtsosp.getText());
                if (CheckContainMaNV(txtmanv.getText()) == true)
                {
                    NhanVien NV = new NhanVien();
                    NV.setMaNV(txtmanv.getText());
                    NV.setPhanXuong(GetValuePhanXuong());
                    NV.setSoSP(Integer.parseInt(txtsosp.getText()));

                    DefaultTableModel model = (DefaultTableModel) table.getModel();
                    model.addRow(new Object[] {txtmanv.getText(),GetValuePhanXuong(),txtsosp.getText(),NV.VuotChuan()});
                    table.setModel(model);
                }
                else
                {
                    JOptionPane.showMessageDialog(this, "This MaNV already been taken !");
                }
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(this, "Please input number into SoSP !");
            }
            
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please input all the field !");
        }
    }//GEN-LAST:event_btnthemActionPerformed

